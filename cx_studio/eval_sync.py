"""Synchronize evaluations between AutoAgent and CX Agent Studio."""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any


@dataclass
class CxEvalDataset:
    """A named collection of evaluation cases in CX Agent Studio format.

    Mirrors the CX Agent Studio ``examples`` (few-shot) or test-case resources
    used to evaluate agent quality.
    """

    dataset_id: str
    display_name: str
    entries: list[dict] = field(default_factory=list)
    metadata: dict = field(default_factory=dict)


class CxEvalSync:
    """Synchronise evaluation data between AutoAgent and CX Agent Studio.

    Handles bidirectional conversion of eval cases and results, and exposes
    helpers for CX AI-assisted test generation.

    Args:
        client: Optional ``CxClient`` instance used for API-driven operations
            such as ``generate_evaluation``.  When ``None`` the sync operates
            in offline mode.
    """

    def __init__(self, client: Any = None) -> None:
        self._client = client

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def export_eval_dataset(
        self,
        cases: list[dict[str, Any]],
        dataset_name: str,
    ) -> CxEvalDataset:
        """Convert AutoAgent evaluation cases to a CX-format eval dataset.

        Each AutoAgent eval case has the shape::

            {
                "input": "<user utterance>",
                "expected_output": {<dict>},
                "tags": ["tag1", "tag2"],   # optional
                "context": {<dict>},        # optional
            }

        The resulting ``CxEvalDataset.entries`` list contains CX-formatted
        conversation turns ready to be pushed to CX Agent Studio.

        Args:
            cases: List of AutoAgent eval case dicts.
            dataset_name: Human-readable name for the resulting dataset.

        Returns:
            A ``CxEvalDataset`` with all cases converted to CX format.
        """
        entries: list[dict[str, Any]] = []
        for idx, case in enumerate(cases):
            cx_entry = self.map_eval_case_to_cx(case)
            cx_entry.setdefault("name", f"{dataset_name}_case_{idx + 1}")
            entries.append(cx_entry)

        dataset_id = _slugify(dataset_name)
        return CxEvalDataset(
            dataset_id=dataset_id,
            display_name=dataset_name,
            entries=entries,
            metadata={
                "source": "autoagent_eval",
                "total_cases": len(cases),
            },
        )

    def import_eval_results(
        self,
        cx_results: dict[str, Any],
    ) -> list[dict[str, Any]]:
        """Import CX evaluation results back into AutoAgent format.

        CX evaluation results typically have the shape returned by the CX
        ``runTestCase`` or batch evaluation APIs.

        Args:
            cx_results: Raw CX evaluation results dict (API response shape).

        Returns:
            List of AutoAgent-format result dicts, each with ``"input"``,
            ``"actual_output"``, ``"expected_output"``, ``"passed"``, and
            ``"cx_test_case_name"`` keys.
        """
        results: list[dict[str, Any]] = []
        for result in cx_results.get("results", cx_results.get("testCaseResults", [])):
            results.append(self.map_cx_result_to_autoagent(result))
        return results

    def generate_evaluation(
        self,
        agent_id: str,
        description: str,
    ) -> list[dict[str, Any]]:
        """Leverage CX AI-assisted test generation to create eval cases.

        When a CX client is available, triggers the CX Agent Studio test
        generation API for the specified agent.  Falls back to returning an
        empty list with a metadata entry when operating offline.

        Args:
            agent_id: Fully-qualified CX agent resource name.
            description: Natural-language description of the test scenarios to
                generate (passed to the CX generation API as a prompt).

        Returns:
            List of AutoAgent-format eval case dicts generated by CX.
        """
        if self._client is None:
            return [
                {
                    "input": "",
                    "expected_output": {},
                    "tags": ["ai_generated"],
                    "_meta": {
                        "generated": False,
                        "reason": "No CX client available for AI-assisted generation.",
                        "description": description,
                    },
                }
            ]

        try:
            data = self._client._request(
                "POST",
                f"https://ces.googleapis.com/v1/{agent_id}:generateTestCases",
                json_body={"description": description},
            )
        except Exception as exc:
            return [
                {
                    "input": "",
                    "expected_output": {},
                    "tags": ["ai_generated"],
                    "_meta": {
                        "generated": False,
                        "reason": str(exc),
                        "description": description,
                    },
                }
            ]

        generated = data.get("testCases", []) if data else []
        return [self.map_cx_result_to_autoagent(tc) for tc in generated]

    def map_eval_case_to_cx(self, case: dict[str, Any]) -> dict[str, Any]:
        """Map a single AutoAgent eval case to CX example/test-case format.

        CX conversation turns have the shape::

            {
                "conversationTurns": [
                    {
                        "userInput": {"input": {"text": {"text": "<utterance>"}}},
                        "virtualAgentOutput": {"currentPage": {}, "text": []}
                    }
                ]
            }

        Args:
            case: A single AutoAgent eval case dict.

        Returns:
            CX-formatted dict representing the example or test case.
        """
        user_text = case.get("input", "")
        expected = case.get("expected_output", {})
        tags = case.get("tags", [])
        context = case.get("context", {})

        # Build the user input turn
        user_turn: dict[str, Any] = {
            "userInput": {
                "input": {
                    "text": {"text": user_text},
                    "languageCode": context.get("language_code", "en"),
                },
                "injectedParameters": context.get("injected_parameters", {}),
            },
            "virtualAgentOutput": _build_virtual_agent_output(expected),
        }

        cx_case: dict[str, Any] = {
            "displayName": case.get("display_name", case.get("_cx_display_name", "")),
            "tags": list(tags),
            "conversationTurns": [user_turn],
        }

        # Preserve any original CX metadata for round-trip fidelity
        if "_cx_test_case_name" in case:
            cx_case["name"] = case["_cx_test_case_name"]

        return cx_case

    def map_cx_result_to_autoagent(self, result: dict[str, Any]) -> dict[str, Any]:
        """Map a single CX test case / evaluation result to AutoAgent format.

        Handles both CX test-case results (``testCaseResults``) and raw test
        cases as returned by the AI generation API.

        Args:
            result: A single CX test case or result dict.

        Returns:
            AutoAgent eval-format dict with ``"input"``, ``"expected_output"``,
            ``"actual_output"``, ``"passed"``, and ``"cx_test_case_name"`` keys.
        """
        # Extract user utterance from conversation turns
        turns = result.get("conversationTurns", result.get("testCaseConversationTurns", []))
        user_text = _extract_user_text(turns)

        # Expected output from test case definition
        expected = result.get("expectedOutput", result.get("lastTestResult", {}))
        if not expected and turns:
            last_turn = turns[-1]
            expected = last_turn.get("virtualAgentOutput", {})

        # Actual output from test result
        actual = result.get("actualOutput", result.get("testResult", {}))

        # Determine pass/fail from CX result status
        test_result = result.get("lastTestResult", {})
        cx_status = test_result.get("testResult", result.get("testResult", ""))
        passed = cx_status in ("TEST_RESULT_PASSED", "PASSED", True)

        return {
            "input": user_text,
            "expected_output": expected,
            "actual_output": actual,
            "passed": passed,
            "tags": list(result.get("tags", [])),
            "cx_test_case_name": result.get("name", ""),
            "_cx_display_name": result.get("displayName", ""),
            "_cx_status": cx_status,
        }


# ---------------------------------------------------------------------------
# Private helpers
# ---------------------------------------------------------------------------


def _slugify(name: str) -> str:
    """Convert a display name to a URL-safe slug ID."""
    import re

    return re.sub(r"[^a-z0-9_]", "_", name.lower().strip())[:64]


def _extract_user_text(turns: list[dict[str, Any]]) -> str:
    """Extract the last user utterance from CX conversation turns."""
    last_text = ""
    for turn in turns:
        user_input = turn.get("userInput", {})
        inp = user_input.get("input", {})
        text_block = inp.get("text", {})
        text = text_block.get("text", "")
        if text:
            last_text = text
    return last_text


def _build_virtual_agent_output(expected: dict[str, Any]) -> dict[str, Any]:
    """Build a CX virtualAgentOutput block from AutoAgent expected_output dict."""
    if not expected:
        return {}

    output: dict[str, Any] = {}

    # Map 'text' or 'response' field to CX text response
    text = expected.get("text", expected.get("response", ""))
    if text:
        output["text"] = [text] if isinstance(text, str) else text

    # Map 'current_page' to CX page reference
    page = expected.get("current_page", expected.get("currentPage", ""))
    if page:
        output["currentPage"] = {"displayName": page}

    # Map 'parameters' / 'session_params' to CX session parameters
    params = expected.get("parameters", expected.get("session_params", {}))
    if params:
        output["sessionParameters"] = params

    return output
