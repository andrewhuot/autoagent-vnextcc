def _make_card(
    card_id: str = "",
    title: str = "Rewrite root prompt",
    status: str = "pending",
    created_at: float | None = None,
) -> ProposedChangeCard:
    """Build a minimal ProposedChangeCard for tests."""
    return ProposedChangeCard(
        card_id=card_id,
        title=title,
        why="Quality improved with clearer instructions",
        diff_hunks=[
            DiffHunk(
                hunk_id="h1",
                surface="prompts.root",
                old_value="Be helpful.",
                new_value="Be helpful and concise.",
                status="pending",
            )
        ],
        metrics_before={"quality": 0.70, "safety": 1.0},
        metrics_after={"quality": 0.80, "safety": 1.0},
        confidence=ConfidenceInfo(p_value=0.03, effect_size=0.1),
        risk_class="low",
        cost_delta=0.001,
        experiment_card_id="exp-001",
        status=status,
        created_at=created_at or time.time(),
    )


def _make_experiment_card(
    experiment_id: str = "exp-001",
    hypothesis: str = "Rewrite root prompt for clarity",
) -> ExperimentCard:
    """Build a minimal ExperimentCard for conversion tests."""
    return ExperimentCard(
        experiment_id=experiment_id,
        created_at=time.time(),
        hypothesis=hypothesis,
        touched_surfaces=["instruction"],
        touched_agents=["root"],
        diff_summary="Rewrote root prompt",
        eval_set_versions={"golden": "abc123"},
        replay_set_hash="hash123",
        baseline_sha="sha_base",
        candidate_sha="sha_cand",
        risk_class="low",
        deployment_policy="pr_only",
        rollback_handle="rollback_001",
        total_experiment_cost=0.05,
        status="accepted",
        result_summary="Quality improved by 14%",
        operator_name="instruction_rewrite",
        baseline_scores={"quality": 0.70, "safety": 1.0, "cost": 0.002},
        candidate_scores={"quality": 0.80, "safety": 1.0, "cost": 0.003},
        significance_p_value=0.03,
        significance_delta=0.1,
    )


