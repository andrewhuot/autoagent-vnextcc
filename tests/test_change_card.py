class TestProposedChangeCard:
    def test_auto_generates_card_id(self) -> None:
        card = ProposedChangeCard(title="test")
        assert card.card_id != ""
        assert len(card.card_id) == 8

    def test_preserves_explicit_card_id(self) -> None:
        card = ProposedChangeCard(card_id="my-id", title="test")
        assert card.card_id == "my-id"

    def test_to_dict_round_trip(self) -> None:
        card = _make_card(card_id="rt-001")
        data = card.to_dict()
        restored = ProposedChangeCard.from_dict(data)

        assert restored.card_id == "rt-001"
        assert restored.title == card.title
        assert restored.why == card.why
        assert len(restored.diff_hunks) == 1
        assert restored.diff_hunks[0].surface == "prompts.root"
        assert restored.metrics_before == card.metrics_before
        assert restored.metrics_after == card.metrics_after
        assert restored.confidence.p_value == pytest.approx(0.03)
        assert restored.confidence.effect_size == pytest.approx(0.1)
        assert restored.risk_class == "low"
        assert restored.status == "pending"
        assert restored.experiment_card_id == "exp-001"

    def test_from_dict_defaults(self) -> None:
        card = ProposedChangeCard.from_dict({})
        # card_id auto-generated by __post_init__ when empty string passed
        assert len(card.card_id) == 8
        assert card.status == "pending"
        assert card.confidence.p_value == 1.0

    def test_from_experiment_card(self) -> None:
        exp = _make_experiment_card()
        card = ProposedChangeCard.from_experiment_card(exp)

        assert card.title == exp.hypothesis
        assert card.why == exp.result_summary
        assert card.experiment_card_id == "exp-001"
        assert card.risk_class == "low"
        assert card.confidence.p_value == pytest.approx(0.03)
        assert card.confidence.effect_size == pytest.approx(0.1)
        assert card.cost_delta == pytest.approx(0.001)
        assert len(card.diff_hunks) == 1
        assert "instruction" in card.diff_hunks[0].surface
        assert "5%" in card.rollback_condition  # low risk -> 5% threshold

    def test_from_experiment_card_high_risk(self) -> None:
        exp = _make_experiment_card()
        exp.risk_class = "high"
        card = ProposedChangeCard.from_experiment_card(exp)
        assert "3%" in card.rollback_condition  # high risk -> 3% threshold

    def test_from_experiment_card_with_memory_context(self) -> None:
        exp = _make_experiment_card()
        card = ProposedChangeCard.from_experiment_card(
            exp, memory_context="Focus on conciseness"
        )
        assert card.memory_context == "Focus on conciseness"

    def test_from_experiment_card_override_scores(self) -> None:
        exp = _make_experiment_card()
        card = ProposedChangeCard.from_experiment_card(
            exp,
            baseline_scores={"quality": 0.5},
            candidate_scores={"quality": 0.9},
        )
        assert card.metrics_before == {"quality": 0.5}
        assert card.metrics_after == {"quality": 0.9}

    def test_from_experiment_card_no_diff_summary(self) -> None:
        exp = _make_experiment_card()
        exp.diff_summary = ""
        card = ProposedChangeCard.from_experiment_card(exp)
        assert len(card.diff_hunks) == 0

    def test_from_experiment_card_uses_hypothesis_as_fallback_why(self) -> None:
        exp = _make_experiment_card()
        exp.result_summary = ""
        card = ProposedChangeCard.from_experiment_card(exp)
        assert card.why == exp.hypothesis


