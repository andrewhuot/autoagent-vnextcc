class TestChangeCardStore:
    def test_save_and_get(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        card = _make_card(card_id="store-001")
        store.save(card)

        retrieved = store.get("store-001")
        assert retrieved is not None
        assert retrieved.card_id == "store-001"
        assert retrieved.title == card.title
        assert len(retrieved.diff_hunks) == 1

    def test_get_nonexistent(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        assert store.get("nope") is None

    def test_list_pending(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        now = time.time()
        store.save(_make_card(card_id="p1", status="pending", created_at=now))
        store.save(
            _make_card(card_id="p2", status="pending", created_at=now + 1)
        )
        store.save(
            _make_card(card_id="a1", status="applied", created_at=now + 2)
        )

        pending = store.list_pending()
        assert len(pending) == 2
        assert all(c.status == "pending" for c in pending)
        # Newest first
        assert pending[0].card_id == "p2"

    def test_list_all(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        now = time.time()
        store.save(_make_card(card_id="c1", status="pending", created_at=now))
        store.save(
            _make_card(card_id="c2", status="applied", created_at=now + 1)
        )
        store.save(
            _make_card(card_id="c3", status="rejected", created_at=now + 2)
        )

        all_cards = store.list_all()
        assert len(all_cards) == 3
        assert all_cards[0].card_id == "c3"

    def test_update_status(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        store.save(_make_card(card_id="us-001", status="pending"))

        result = store.update_status("us-001", "rejected", reason="Too risky")
        assert result is True

        card = store.get("us-001")
        assert card is not None
        assert card.status == "rejected"
        assert card.rejection_reason == "Too risky"

    def test_update_status_nonexistent(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        assert store.update_status("nope", "applied") is False

    def test_update_hunk_status(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        store.save(_make_card(card_id="uh-001"))

        result = store.update_hunk_status("uh-001", "h1", "accepted")
        assert result is True

        card = store.get("uh-001")
        assert card is not None
        assert card.diff_hunks[0].status == "accepted"

    def test_update_hunk_status_bad_hunk_id(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        store.save(_make_card(card_id="uh-002"))
        assert store.update_hunk_status("uh-002", "bad-id", "accepted") is False

    def test_update_hunk_status_bad_card_id(self, tmp_path: Path) -> None:
        store = ChangeCardStore(db_path=str(tmp_path / "cards.db"))
        assert store.update_hunk_status("nope", "h1", "accepted") is False


